/*********************************************************************************************
CREATE TABLES IN DATABASE
*********************************************************************************************/
CREATE TABLE TBL_CUSTOMER (
CUST_ID_CUS INT IDENTITY(100001, 1) PRIMARY KEY,
FNAME_CUS VARCHAR(50) NOT NULL,
LNAME_CUS VARCHAR(50) NOT NULL,
DOB_CUS DATE NOT NULL DEFAULT '01/01/2000',
PHONE_CUS CHAR(12) DEFAULT '000-0000-000',
EMAIL_CUS VARCHAR(70) UNIQUE
)
GO

CREATE TABLE TBL_HOUSE (
HOUSE_ID_HSE INT IDENTITY(200001, 1),
ADDRESS_HSE VARCHAR(150) NOT NULL,
CITY_HSE VARCHAR(50) NOT NULL,
COUNTRY_HSE VARCHAR(50) NOT NULL,
CUST_ID_HSE INT NOT NULL REFERENCES TBL_CUSTOMER (CUST_ID_CUS),
CONSTRAINT PK_KEY PRIMARY KEY (HOUSE_ID_HSE, CUST_ID_HSE)
)
GO

CREATE TABLE TBL_WORK_ORDER (
ORD_ID_WO INT IDENTITY(3000001, 1) PRIMARY KEY,
CUST_ID_WO INT NOT NULL,
HOUSE_ID_WO INT NOT NULL,
TYPE_WO VARCHAR(10) NOT NULL,
CREATE_DTE_WO DATE NOT NULL,
COMPL_DTE_WO DATE NOT NULL DEFAULT '01/01/2000',
STATUS_WO VARCHAR(5) NOT NULL,
CONSTRAINT FK_KEY_WO FOREIGN KEY(HOUSE_ID_WO, CUST_ID_WO) REFERENCES TBL_HOUSE(HOUSE_ID_HSE, CUST_ID_HSE)
)
GO

CREATE TABLE TBL_COMPLAINT_ORDER (
ORD_ID_CO INT IDENTITY(4000001, 1) PRIMARY KEY,
CUST_ID_CO INT NOT NULL,
HOUSE_ID_CO INT NOT NULL,
TYPE_CO VARCHAR(10) NOT NULL,
CREATE_DTE_CO DATE NOT NULL,
COMPL_DTE_CO DATE NOT NULL DEFAULT '01/01/2000',
STATUS_CO VARCHAR(5) NOT NULL,
CONSTRAINT FK_KEY_CO FOREIGN KEY(HOUSE_ID_CO, CUST_ID_CO) REFERENCES TBL_HOUSE(HOUSE_ID_HSE, CUST_ID_HSE)
)
GO

CREATE TABLE TBL_ORDER_ITEM (
ORD_ID_ITM INT NOT NULL REFERENCES TBL_WORK_ORDER(ORD_ID_WO),
CHANNEL_ITM VARCHAR (20) NOT NULL,
GENRE_ITM VARCHAR(10) NOT NULL CHECK(GENRE_ITM IN ('NEWS', 'MOVIE', 'SPORTS', 'MUSIC')),
PRICE_ITM INT NOT NULL
)
GO

